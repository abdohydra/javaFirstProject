    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     try {
        String username = txtusername.getText();
        String password = txtpassword.getText();
        Connection conn = SingletonConnection.getConnection();
        String SQL = "SELECT * FROM saler WHERE login = ? AND password = ?";
        PreparedStatement pstmt = conn.prepareStatement(SQL);
        pstmt.setString(1, username); 
        pstmt.setString(2, password);
        ResultSet resultSet = pstmt.executeQuery();
        if (resultSet.next()) {
            String retrievedUsername = resultSet.getString("login");
            String retrievedPassword = resultSet.getString("password");
            if ("admin".equals(retrievedUsername) && "1234".equals(retrievedPassword)) {
                adminFr admin = new adminFr();
                admin.setVisible(true);
                this.setVisible(false);
            } else {
                Salerframe saler = new Salerframe();
                saler.jLabel10.setText(retrievedUsername);
                saler.setVisible(true);
                this.setVisible(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Invalid username or password. Please try again.", "Login Failed", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

